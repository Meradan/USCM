<h2 class="heading heading-h2">USCM Roleplaying game</h2>

<h3 class="heading heading-h3">Swedish info</h3>

Det här är en rollspelskampanj i militär science fiction-miljö inspirerad främst av Alien-filmerna, men även Starship Troopers, XCOM m.m. Spelarnas karaktärer är soldater i framtidens motsvarighet till den amerikanska marinkåren. Stämningen i kampanjen inriktas i första hand på action, spänning och taktisk planering, men det kan vara stor variation mellan olika uppdrag.<br><br>

Kampanjen har ingen fast spelargrupp. Den är istället organiserad på ett sätt som låter fler spelare vara med i den.
Varje uppdrag är ett fristående scenario. Alla spelare är med på en maillista och när en spelledare har gjort ett nytt äventyr
skickas ett "announcement" för detta ut på maillistan. Detta innehåller tid och plats, antal spelare samt en kort beskrivning
av uppdraget. Sedan är det först till kvarn. De första spelarna som anmäler sig får en mer detaljerad briefing som bekräftelse
på att de har kommit med på uppdraget.<br> Det gör att det blir variation i spelgruppen från gång till gång och det går bra att
vara med i kampanjen även om man inte kan spela så ofta. Ibland när alldeles för många
har velat vara med på samma uppdrag har det spelats flera gånger med olika spelargrupper.<br><br>

Alla uppdrag som spelats inom kampanjen finns beskrivna under "Missions" med den
briefing-text som spelarna fick ut innan uppdraget och med sammanfattningen som skrivits efteråt av spelledaren eller någon spelare. Scenarion vi kör på konvent brukar inte få egna debriefingar, men dom spelas ofta som ett kampanjmission också.<br><br>

Kampanjen håller mestadels till i Linköping där den startade. Det har periodvis även funnits aktiva spelgrupper (plutoner) på andra orter och på senare tid kör vi ibland missions över mail också. USCM brukar även dyka upp som arrangemang på spelkonventet <a href="http://www.lincon.se" target="_blank">LinCon</a>.<br><br>
Det finns för tillfället plats för fler spelare, kom in på vår Discord om du är intresserad.<br><br>

<h3 class="heading heading-h3">English info</h3>

This is the homepage for a roleplaying game set in a universe inspired mainly by the Alien movies.<br><br>

The goal of this campaign is to mix the atmosphere of the Alien movies with a military-style RPG campaign where
we focus on action and tactical planning.<br><br>

Most roleplaying sessions are done in the traditional tabletop style, but there are occasional PBM or online scenarios as well.<br><br>

