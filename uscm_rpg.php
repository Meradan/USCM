<div class="title">English Info</div>
This is the homepage for a roleplaying campaign based on the Alien movies. The players are members of the 5th platoon 
"Brixton's Fightin' Fives" in the 4th US Colonial Marine brigade.<BR><BR>

The goal of this campaign is to mix the atmosphere of the Alien movies with a military-style RPG campaign where 
we focus on action and tactical planning.<BR><BR>

All roleplaying is done in the "traditional" way with paper and dice - there is no online roleplaying. Because of that only
swedish players can join the campaign and most content on this webpage is in swedish.<BR><BR>

