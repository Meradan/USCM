<div class="title">Swedish Info</div>

Rollspelskampanjen som den här sidan handlar om utspelar sig i samma miljö som Alien-filmerna. Spelarnas karaktärer är 
soldater i framtidens motsvarighet till den amerikanska marinkåren och tillhör plutonen Brixton's Fightin' Fives. 
Stämningen i kampanjen inriktas i första hand på action, spänning och taktisk planering.<BR><BR>

Kampanjen har ingen fast spelargrupp. Den är istället organiserad på ett sätt som låter många fler spelare vara med i den. 
Varje uppdrag är ett fristående äventyr. Alla spelare är med på en maillista och när en spelledare har gjort ett nytt äventyr 
skickas ett "announcement" för detta ut på maillistan. Detta innehåller tid och plats, antal spelare samt en kort beskrivning 
av uppdraget. Sedan är det först till kvarn. De första spelarna som anmäler sig får en mer detaljerad briefing som bekräftelse 
på att dom har kommit med på uppdraget.<BR> Det gör att det blir variation i spelgruppen från gång till gång och det går bra att 
vara med i kampanjen även om man inte kan spela så ofta. I de fall där alldeles för många 
har velat vara med på ett uppdrag har det spelats två gånger med olika spelargrupper.<BR><BR>

Första uppdraget i kampanjen spelades i november år 2000. Därefter har det blivit många fler. Alla uppdrag finns beskrivna under "missions" med den 
briefing-text som spelarna fick ut innan uppdraget och med sammanfattningen som skrivits efteråt av spelledaren och ibland av 
någon spelare.<BR><BR>

USCM-kampanjen har legat i dvala ett antal år även om den har dykt upp som arrangemang på spelkonventet LinCon ibland. 
Men den är inte död, den finns kvar någonstans där ute i rymdens kalla mörker och kanske väntar på att få döda just din karaktär..<BR><BR>

